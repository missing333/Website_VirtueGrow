    <div id="Humility" class="virtueContent" style="display:none">
      <div class="title">
        <h2>Humility</h2>
      </div>
      <div class="quote">
        <p class="quotation">
          True humility is not thinking less of yourself; it is thinking of yourself less.</p>
        <author>— Rick Warren</author>
      </div>
      <div class="description shadowbox">
        <h4>Seeds of Humility</h4>
        <p>
          Humility is the ability to recognize that while Truth is infallible, the human mind is not. It is the ability to accept criticism with a desire to improve, coupled with the ability to learn lessons from the most unlikely of teachers.
          </p><p>
          It is the recognition that every single person on the planet has something that they are able to teach you, and likewise, YOU have something unique that you are able to offer to others.
        </p>
      </div>
      <div class="isisnt shadowbox">
        <strong>What it is:</strong> A recognition of objective Truth.<br><br>
        <strong>What it is NOT:</strong> Claiming that you are beneath others, or not worthy of something that you ARE worthy of. <br><br>
      </div>
      <div class="example">
        <strong>Example of Humility:</strong><br>
        <p>Mary saying 'Yes' to the Lord's will for her life.</p>
      </div>


      <div class="oppTitle">
        <h2 style="text-align:center">Opposite of Humility <br>(Pride)</h2>
      </div>
      <div class="oppositeVice shadowbox">
        <p>
          Pride is the disorder of elevating the importance of Self over the importance of Truth. It no longer can recognize that other people have access to God's Truth, and, viewing them as having nothing to offer the Self, discards them as useless.
        </p>
        <p>
          Pride says: "That person is a waste of space.", and "My way is the only way."
        </p>
      </div>
      <div class="harmsYou"><strong>Pride harms you</strong> by creating a barrier to learning (and re-learning) life's lessons.
      </div>
      <div class="harmsOthers shadowbox"><strong>Pride harms others</strong> by reducing them to utilitarian value, stripping them of their spiritual dignity.
      </div>






    </div>
    <div id="Diligence" class="virtueContent" style="display:none">
      <div class="title">
        <h2>Diligence</h2>
      </div>
      <div class="quote">
        <p class="quotation">
          What we hope ever to do with ease, we must learn first to do with diligence.</p>
        <author>— Samuel Johnson</author>
      </div>
      <div class="description shadowbox">
        <p>  Diligence is a commitment to daily spiritual, personal improvement. Having Diligence means that even though you are fully aware that true perfection is unattainable, you forge ahead and do what you can anyways,
        because it is the right thing to do.
        </p>
        <p>
        Diligence unlocks the beauty and the holiness of the seemingly mundane. Whether always remembering to give thanks before a meal, or reading the bible even when tired, these seemingly small acts are large to God. Why
        should one be trusted in large matters if they can not be trusted in small matters?
      </p>
      </div>
      <div class="isisnt shadowbox">
        <strong>What it is:</strong> A willingness to act on a desire to learn and grow. <br><br>
        <strong>What it is NOT:</strong> Hard work for the sake of material gain.
      </div>
      <div class="example"></div>

      <div class="oppTitle">
        <h2>Opposite of Diligence <br>(Sloth)</h2>
      </div>
      <div class="oppositeVice shadowbox">
        <p>
          Sloth is the abuse or negligence of the fruitfulness of work. Where Diligence multiplies, Sloth shrivels. Sloth seeks to avoid personal growth, often hiding in distractions and mindless entertainment. Sloth can even use the deceptive mask of long work hours, if it means that your soul is not being nourished and growing closer to God.
        </p>
      </div>
      <div class="harmsYou">
        <strong>Sloth harms you</strong> by making the chasm between an individual and God seem impassable. <br><br>
      </div>
      <div class="harmsOthers shadowbox">
        <strong>Sloth harms others</strong> by setting a bad example for a holy life.
      </div>
    </div>




    <div id="Generosity" class="virtueContent" style="display:none">
      <div class="title">
        <h2>Generosity</h2>
      </div>
      <div class="quote">
        <p class="quotation">
          We often say, ‘I would give, but only to the deserving.’ The trees in your orchard say not so, nor the flocks in your pasture. They give that they may live, for to withhold is to perish.</p>
        <author>— Kahlil Gibran</author>
      </div>
      <div class="description shadowbox">
        <p>
          Generosity is the great multiplier of goodwill. When someone receives a gift out of Generosity, it inspires something in them to give Generously to others as well. In this way, Generosity can grow and spread like wildfire, lighting the flames
        of goodwill in humanity.
      </p>
      </div>
      <div class="isisnt shadowbox">
        <strong>What it is:</strong> Joy in a gift of self to another.<br><br>
        <strong>What it is NOT:</strong> Repayment, nor giving out of perceived obligation.
      </div>
      <div class="example"></div>

      <div class="oppTitle">
        <h2>Opposite of Generosity<br>(Greed)</h2>
      </div>
      <div class="oppositeVice shadowbox">
        <p>
          Greed is the selfish desire to contain and control the things of this world, especially those things that give power such as money or fame. Where Generosity seeks to spread and distribute good and goodwill, Greed seeks to take and contain
        those things. Greed is a black hole that can never be filled. </p>
      </div>
      <div class="harmsYou">
        <strong>Greed harms you</strong> by creating an insatiable hunger for base things. This hunger ultimately consumes the one who tries to feed it, making them slaves to the things they try to possess.
      </div>
      <div class="harmsOthers shadowbox">
        <strong>Greed harms others</strong> by withholding from them the things that they need to survive: food, money, freedom.
      </div>
    </div>




    <div id="Patience" class="virtueContent" style="display:none">
      <div class="title">
        <h2>Patience</h2>
      </div>
      <div class="quote">
        <p class="quotation">
          Patience is not simply the ability to wait - it's how we behave while we're waiting.</p>
        <author>— Joyce Meyer</author>
      </div>
      <div class="description shadowbox">
        <p>Patience is the great bridge builder. While Anger destroys relationships with others, Patience restores them. Patience is the quiet strength that is able to withstand the pains of others, along with your own, for noble purpose.</p>
      </div>
      <div class="isisnt shadowbox">
        <strong>What it is:</strong> The ability to endure burdens, suffering, or aggression without complaint or seeking retaliation.<br><br>
        <strong>What it is NOT:</strong> Distracting yourself while you wait for something.
      </div>
      <div class="example"></div>

      <div class="oppTitle">
        <h2>Opposite of Patience <br>(Anger)</h2>
      </div>
      <div class="oppositeVice shadowbox">
        <p>
          Anger is the sin of seeking justice in a disordered way. Anger wants to disproportionately harm others for even the slightest transgressions. Alternatively, Anger is willing to seek just punishment on someone who is innocent, which is also
          disordered.
        </p>
      </div>
      <div class="harmsYou">
        <strong>Anger harms individuals</strong> by turning them into unjust judges and executors of punishment.
      </div>
      <div class="harmsOthers shadowbox">
        <strong>Anger harms others,</strong> often physically or emotionally, because it desires to punish with no concern for the victim.
      </div>
    </div>



    <div id="Kindness" class="virtueContent" style="display:none">
      <div class="title">
        <h2>Kindness</h2>
      </div>
      <div class="quote">
        <p class="quotation">
          Kindness begins with the understanding that we all struggle.</p>
        <author>— Charles Glassman</author>
      </div>
      <div class="description shadowbox">
        <p>Kindness is the virtue of showing love and solidarity to others. It is the virtue of letting others know that they are not alone, that they are part of something greater than themselves, part of a friendship or of a community.</p>
      </div>
      <div class="isisnt shadowbox">
        <strong>What it is:</strong> The ability to share in the joys and sorrows of others. <br><br>
        <strong>What it is NOT:</strong> Being a pushover.
      </div>
      <div class="example"></div>

      <div class="oppTitle">
        <h2>Opposite of Kindness <br>(Envy)</h2>
      </div>
      <div class="oppositeVice shadowbox">
        <p>Envy is the sin of finding joy in another's pain, and anger at someone else's joy or good fortune. Envy is comparing yourself, your life, your house, or any other attribute to others, and when finding yourself lacking, leads to unkind
        thoughts about them, or whether they 'deserve' those things.</p>
      </div>
      <div class="harmsYou">
        <strong>Envy harms you</strong> by causing rifts in your life-giving friendships.
      </div>
      <div class="harmsOthers shadowbox">
        <strong>Envy harms others</strong> by withholding genuine love and friendship to those who may need it.
      </div>
    </div>




    <div id="Moderation" class="virtueContent" style="display:none">
      <div class="title">
        <h2>Moderation</h2>
      </div>
      <div class="quote">
        <p class="quotation">
          In moderating, not in satisfying desires, lies peace.</p>
        <author>— Reginald Heber</author>
      </div>
      <div class="description shadowbox">
        <p>Most Sin is a disordered twist on a Virtue, taking it to an extreme of too little or too much. Moderation is the Virtue that strives towards healthy balance.</p>
      </div>
      <div class="isisnt shadowbox">
        <strong>What it is:</strong> A balance between the extremes of too little and too much. <br><br>
        <strong>What it is NOT:</strong> Total abstinence of food or drink.
      </div>
      <div class="example"></div>

      <div class="oppTitle">
        <h2>Opposite of Moderation<br>(Gluttony)</h2>
      </div>
      <div class="oppositeVice shadowbox">
        Gluttony is primarily the abuse of food and drink. Overeating is just one way to abuse these. There are other ways to be Gluttonous as well:
        <ul class="contentUL">
          <li>Laute - eating food that is too luxurious, exotic, or costly</li>
          <li>Studiose - eating food that is excessive in quality (too daintily or elaborately prepared)</li>
          <li>Nimis - eating food that is excessive in quantity (too much)</li>
          <li>Praepropere - eating hastily (too soon or at an inappropriate time)</li>
          <li>Ardenter - eating greedily (too eagerly)</li>
        </ul>
      </div>
      <div class="harmsYou">
        <strong>Gluttony harms you</strong> by keeping you from self-mastery, by eroding your self control.
      </div>
      <div class="harmsOthers shadowbox">
        <strong>Gluttony harms others</strong> by craving more, creating excess waste, and leaving less for the needy.
      </div>
